package Calculator2;

public class CalculatorNew {


    public int sum(int x , int y){
        int result = x + y;
        return result;
    }

    public int mul(int x , int y){
        int result = x * y;
        return result;
    }

    public int div(int z , int p){
        int result = z / p;
        return result;
    }

    public int sub(int x , int y , int z){
        int result = x - y - z;
        return result;
    }

}
